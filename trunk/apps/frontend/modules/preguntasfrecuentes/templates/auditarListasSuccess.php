    <div class="header-left">             <div class="header-right"></div>         </div><div class="top-left">
        <div class="top-right">
            <p>
                Una carta de recomendación consiste en una <strong>valoración positiva</strong> de un profesional que te ha producido una <?php echo link_to('experiencia de cliente plenamente satisfactoria', 'preguntasfrecuentes/contribuir#C', array('title' => '¿Qué es una experiencia de cliente satisfactoria?')); ?>, única e irrepetible por sus buenas prácticas y excelencia en el servicio que te ha prestado.
            </p>
            <p>
                Recomiendas a un profesional cuando los aspectos del servicio que consideras importantes (conocimientos, atención, trato, etc.) <strong>se ajustan a tus necesidades y expectativas.</strong>
            </p>
            <p>
                Un profesional es publicado en el Directorio de buenos profesionales cuando es recomendado por 1 colaborador. A partir de ese momento, puede ser auditado (recomendado o desaprobado) por la comunidad de <span class="nosotros_auditoscopia">audit<span class="auditoscopia_o">o</span>scopia</span>.
            </p>
            <p>
                Una carta de recomendación consta de los datos del profesional y los <strong>motivos</strong> por las cuales le recomiendas.
            </p>
            <p>
                Para que una carta de recomendación aporte valor, es necesario que incluya las <strong>razones</strong> por las cuales consideras que el profesional te ha prestado un servicio excelente.
            </p>
            <p>
                Para crear una <strong>carta de recomendación</strong>, puedes elegir entre estas opciones:
            </p>
            <ul>
                <li>
                    Si el profesional no está publicado en el Directorio, entra en "Recomienda un profesional" y <strong>dale de alta.</strong>
                </li>
                <li>
                    Si el profesional ya está publicado, búscalo en el Directorio y haz clic en <strong>recomienda.</strong>
                </li>
                <li>
                    También puedes dar de alta o recomendar a un profesional ya publicado si nos envías un correo electrónico, con los motivos por los cuales lo recomiendas a <a href="mailto:buenprofesional@auditoscopia.com" title="buenprofesional@auditoscopia.com">buenprofesional@auditoscopia.com</a>
                </li>
            </ul>
        </div>
