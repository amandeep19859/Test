<section class="border-box-n">
    <a name="E" id="E"></a>
    <h2 class="faq">¿Cómo auditar en la lista blanca?</h2>
    <div class="header-left">             <div class="header-right"></div>         </div><div class="top-left">
        <div class="top-right">
            <p>
                <strong>Auditar = evaluar tu experiencia de cliente</strong>
            </p>
            <p>
                Auditar consiste en <strong>evaluar la experiencia de cliente</strong> que una empresa, entidad pública o producto de la <?php echo link_to("lista blanca", "nosotros/listablanca", array('title'=>'Qué es la lista blanca de la Excelencia')) ?> te produce como usuario.
            </p>
            <p>
                Para auditar, sólo necesitas entrar en la lista blanca, buscar la empresa, entidad o producto de tu interés y hacer clic en <strong>audita</strong>.
            </p>
            <p>
                Para saber más sobre cómo auditar visita nuestros <?php echo link_to("consejos para auditar", "nosotros/consejos?page=5", array('title' => 'Consejos para auditar')) ?>.
            </p>
            <span style="display: block; width: 100%; height: 8px;"></span>
            <p>
                <strong style="font-size: 16px;">¿Qué objetivos persigue la auditoría?</strong>
            </p>
            <ul>
                <li>
                    <strong>Valorar el éxito de la implantación</strong> del Plan de acción de mejora propuesto y observar la evolución de los indicadores de excelencia establecidos.
                </li>
                <li>
                    <strong>Asegurar la Excelencia</strong> que garantice una experiencia de cliente única y memorable.
                </li>
                <li>
                    <strong>Detectar las posibles desviaciones</strong> que pudieran ocurrir y proponer medidas de mejora de manera dinámica.
                </li>
                <li>
                    <strong>Descubrir nuevas necesidades</strong> por parte de los consumidores para ponerlas en conocimiento de las empresas y entidades.
                </li>
                <li>
                    <strong>Reajustar las necesidades</strong> de los usuarios y la capacidad de las empresas y entidades para satisfacerlas.
                </li>
            </ul>
            <p class="encabezado_acordeon" style="font-size: 16px;">
                <strong title="Medición de resultados y categorías de excelencia">Medición de resultados y categorías de excelencia</strong>
            </p>
            <div class="detalle_acordeon">
                <ul>
                    <li>
                        La auditoría de empresas, entidades públicas y productos publicados en la lista blanca se realiza <strong>mediante un sondeo de valoración</strong>, que incluye indicadores de excelencia que hemos definido anteriormente con las empresas o entidades y sus clientes.
                    </li>
                    <li>
                        Cada <strong>sondeo es específico para cada caso</strong> y cuenta con indicadores de excelencia definidos a tal fin, que modificamos proactivamente en respuesta a la evolución de los consumidores o según se detectan y corrigen desviaciones.
                    </li>
                    <li>
                        El sondeo está diseñado con un <strong>algoritmo que tiene en cuenta diferentes variables</strong> (entre ellas, el número de participantes) y que clasifica a las empresas, entidades o productos en tiempo real en categorías de excelencia.
                    </li>
                    <li>
                        Una categoría de excelencia es una parametrización de la <strong>percepción individual de un usuario sobre el nivel de satisfacción y la calidad</strong> de la experiencia de cliente que le produce un producto o servicio.
                    </li>
                    <li>
                        Las <strong>auditorías múltiples</strong> de un colaborador a una misma empresa, entidad o producto cuentan como una <strong>actualización de la valoración anterior</strong> (sumando o restando puntos a los concedidos anteriormente) pero no como una nueva auditoría. Así garantizamos la objetividad y fiabilidad de los resultados.
                    </li>
                    <li>
                        Las empresas, entidades y productos publicados en la lista blanca se clasifican en las <strong>siguientes categorías de excelencia</strong>:
                        <ul style="margin-left: 1cm;">
                            <li style="list-style-type: none; list-style-image: none; background: url('/images/img_listas/medalla-oro.png') no-repeat left; padding: 15px 0px 10px 48px; height: 55px;">
                                <strong>Medalla de oro:</strong> experiencia única e irrepetible. Lo mejor de lo mejor.
                            </li>
                            <li style="list-style-type: none; list-style-image: none; background: url('/images/img_listas/medalla-plata.png') no-repeat left; padding: 20px 0px 10px 48px; height: 40px;">
                                <strong>Medalla de plata:</strong> experiencia interesante y especial. Recomendado.
                            </li>
                            <li style="list-style-type: none; list-style-image: none; background: url('/images/img_listas/medalla-bronce.png') no-repeat left; padding: 20px 0px 20px 48px; height: 40px;">
                                <strong>Medalla de bronce:</strong> experiencia positiva pero normalita. Aceptable.
                            </li>
                        </ul>
                    </li>
                </ul>  
            </div>
            <p class="encabezado_acordeon" style="font-size: 16px;">
                <strong title="Condiciones para auditar en la lista blanca">Condiciones para auditar en la lista blanca</strong>
            </p>
            <div class="detalle_acordeon">
                <ul>
                    <li>
                        Para auditar, sólo necesitas <strong>ser <?php echo link_to("colaborador", "nosotros/glosarioterminos#Colaborador", array('class' => 'glosario', 'title' => 'Colaborador')) ?> y usuario</strong> de la empresa, entidad o producto que desees valorar.
                    </li>
                    <li>
                        No es necesario haber contribuido anteriormente en el concurso del que ha sido objeto la empresa, entidad o producto, aunque es recomendable <strong>estar informado</strong> sobre su contenido y evolución.
                    </li>
                    <li>
                        El seguimiento de una empresa, entidad o producto es un <strong>proceso continuo</strong> que se extiende durante todo el tiempo en que esté publicado en la lista blanca.
                    </li>
                    <li>
                        Si tu experiencia de cliente cambia, ¡audita! <strong>Puedes auditar 1 vez al mes</strong> (con un intervalo mínimo de 30 días entre cada valoración) por cada empresa, entidad o producto concreto que quieras valorar.
                    </li>
                    <li>
                        Además, puedes auditar <strong>hasta 5 empresas, entidades o productos</strong> diferentes por día.
                    </li>
                </ul>  
            </div>
        </div>
    </div>
