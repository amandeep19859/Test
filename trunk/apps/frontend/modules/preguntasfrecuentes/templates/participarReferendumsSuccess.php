    <h2 class="faq">¿Cómo puedo votar en un concurso?</h2>
    <div class="header-left">             <div class="header-right"></div>         </div><div class="top-left">
        <div class="top-right">
                <ul style="margin-top: 0px;">
                <li>
                    Puedes votar en el Referéndum de un concurso si <strong>has contribuido en el mismo</strong> con una contribución publicada o más.
                </li>
                <li>
                    Como <?php echo link_to("elector", "nosotros/glosarioterminos#Elector", array('class' => 'glosario','title'=>'Elector')) ?>, tienes derecho a dar hasta <strong>cinco votos con</strong> diferente puntuación:
                    <ul>
                        <li>
                            <strong>5 puntos</strong> para la mejor contribución.
                        </li>
                        <li>
                            <strong>4 puntos</strong> para la segunda mejor.
                        </li>
                        <li>
                            <strong>3 puntos</strong> para la tercera mejor.
                        </li>
                        <li>
                            <strong>2 puntos</strong> para la cuarta mejor.
                        </li>
                        <li>
                            <strong>1 punto</strong> para la quinta mejor.
                        </li>
                    </ul>
                </li>
                <li>
                    <strong>Votar es voluntario.</strong> También es voluntario dar los cinco votos. Puedes dar uno, dos, tres, cuatro o los cinco. Si das uno o dos votos, por ejemplo, pueden ser con la puntuación que prefieras: 1 y 3 puntos, 4 y 5 puntos, sólo un voto de 2 puntos, etc.
                </li>
                <li>
                    Tu <strong>voto es secreto</strong>. Ningún colaborador sabe a quién han votado los demás.
                </li>
                <li>
                    Al haber contribuido, en el concurso puedes a la vez votar y ser votado pero <strong>no te puedes votar a ti mismo</strong>.
                </li>
                <li>
                    Solamente puedes <strong>votar una vez a la misma contribución</strong> (con valor de 1, 2, 3, 4 ó 5 puntos).
                </li>
            </ul>
