            <div id="alinea">
                <ul><li>
Los concursos son de <strong>acceso restringido</strong> exclusivamente a usuarios registrados como colaboradores. Si no eres colaborador no podrás visualizar el contenido de esta sección.
                    </li><li>
Al ser un contenido restringido, su <strong>contenido no es indexable</strong> por los buscadores, por lo que las incidencias detectadas únicamente son visibles para colaboradores de <span class="nosotros_auditoscopia">audit<span class="auditoscopia_o">o</span>scopia</span>.
  </li><li>
Puedes contribuir en cualquier concurso que desees, tantas veces como quieras, siempre que contribuyas con un Plan de acción distinto pero no puedes crear concursos nuevos. Todos los <strong>concursos son creados exclusivamente por</strong> <span class="nosotros_auditoscopia">audit<span class="auditoscopia_o">o</span>scopia</span> bajo petición de la empresa o entidad contratante.
  </li><li>
Tu Plan de acción sólo lo puede visualizar <span class="nosotros_auditoscopia">audit<span class="auditoscopia_o">o</span>scopia</span> y la empresa o entidad en concurso.
  </li><li>
Solamente celebramos <strong>un concurso a la vez</strong> por empresa o entidad, con una duración de 90 días y dedicado a un objetivo de mejora específico establecido por la misma.
  </li><li>
Al finalizar el concurso celebramos un Referéndum para elegir las mejores contribuciones, con la misma dinámica que los Referéndums de los concursos regulares.
  </li><li>
<strong>Recompensamos todos los concursos con Participación en beneficio</strong>, que definimos y hacemos público antes de comenzar el concurso y que repartimos siguiendo el modelo establecido en <span class="link_rojo">Quien tiene derecho a participación en beneficio</span> (link a Quien tiene derecho a participación en beneficio). Únicamente dejas de participar en beneficio cuando el concurso es declarado <span class="link_rojo">nulo</span> (link a ¿Cuándo se declara nulo un concurso?).
  </li><li>
La Participación en beneficio que puedes ganar es <strong>variable</strong> y la definimos conjuntamente para cada concurso particular.<br/>
  </li><li>
La empresa o entidad pública en concurso <strong>participa en la gestión del concurso</strong> y en selección de las mejores contribuciones o las que le aportan más valor. Sin embargo no puede votar en los Referéndums ni influir de manera directa en los resultados del mismo.<br/>
  </li><li>
La empresa o entidad en concurso puede igualmente <strong>interactuar con los colaboradores</strong> realizando comentarios y opiniones o solicitando más <span class="azulon">feedback</span> de manera pública o privada si así lo estiman conveniente. En el último caso necesitan contar con la autorización tanto de <span class="nosotros_auditoscopia">audit<span class="auditoscopia_o">o</span>scopia</span> como del propio colaborador.<br/>
  </li><li>
Si tu contribución es de gran valor o aporta una idea genial puedes ser <strong>recompensado adicionalmente</strong> por parte de la empresa o entidad en concurso, quien también puede recompensar como desee a otros colaboradores al margen del resultado producido en el Referéndum.<br/>
  </li><li>
La empresa o entidad en concurso puede ser publicada en la <span class="link_rojo"><?php echo link_to("lista blanca","nosotros/listablanca")?></span> cuando ha permanecido un año o más en la Comunidad Privada y puede demostrar, mediante auditoría realizada por <span class="nosotros_auditoscopia">audit<span class="auditoscopia_o">o</span>scopia</span>, la resolución con éxito del 80% o más de las experiencias insatisfactorias detectadas junto con el establecimiento de una política de mejora continua definida.<br/>
