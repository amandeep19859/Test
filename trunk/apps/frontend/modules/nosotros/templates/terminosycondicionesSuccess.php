    <h2>1. Concursos</h2>
    <section class="border-box-n">        
        <div class="header-left">             <div class="header-right"></div>         </div><div class="top-left">
            <div class="top-right">
                <p>
                    <strong>Funcionamiento de un concurso</strong>
                </p>
                <ul class="clean">
                    <li>
                        <strong>1.1 Contribuciones. Requisitos</strong>
                        <ul class="clean">
                            <li>
                                <strong>1.1.1 Las contribuciones presentadas a concurso, podrán referirse a:</strong>
                                <ul>
                                    <li>
                                        Empresas
                                    </li>
                                    <li>
                                        Entidades públicas
                                    </li>
                                    <li>
                                        Productos
                                    </li>
                                    <li>
                                        Servicios
                                    </li>
                                    <li>
                                        Procesos
                                    </li>
                                </ul>
                            </li>
                            <li>
                                <strong>1.1.2 Requisitos para participar</strong> 
                                <ul class="clean">
                                    <li>
                                        <strong>a) El usuario registrado deberá proponer una contribución, relativa a una empresa, entidad, producto, servicio o proceso</strong>
                                        <br>
                                        <br>
                                        En su contribución deberá adjuntar la denominación social, dirección, localidad y provincia de la empresa o entidad pública; categoría del servicio objeto de la propuesta (por ejemplo, "Atención al cliente", "Servicio post- venta", etc.), o marca y modelo del producto.
                                    </li>
                                    <li style="margin-top: 1.8em;">
                                        <strong>b) La contribución presentada a concurso constará de dos partes:</strong>
                                        <br>
                                        <br>
                                        <strong>- Incidencia o testimonio.</strong> En primera instancia, deberá señalarse la incidencia o experiencia de cliente insatisfactoria detectada por el colaborador en la empresa, entidad, producto, servicio o proceso, como problema o desajuste a sus expectativas.
                                        <br>
                                        <br>
                                        <strong>- Plan de acción o propuesta de mejora.</strong> Deberá contener las medidas de mejora más adecuadas, según la opinión del colaborador, para mejorar el servicio o producto y resolver el problema detectado.  El formato, extensión y profundidad del informe será libre (pudiendo complementarlo con textos, dibujos, fotos, videos, etc.), bajo el único requisito de que las propuestas aporten valor y se realicen mediante una metodología rigurosa y contrastable.
                                        <br>
                                        <br>
                                        Podrán realizarse las contribuciones que se deseen respecto a un mismo producto, entidad o servicio, siempre y cuando sean diferentes y aporten claramente valor adicional. 
                                        <br>
                                        <br>
                                        Las contribuciones que no cumplan los requisitos arriba señalados serán retiradas por AUDITOSCOPIA.
                                        <br>
                                        <br>
                                        En ningún caso se aceptarán contribuciones que contengan términos que vayan en contra el decoro y las buenas maneras. En  especial, quedarán fuera de presentación aquéllas en los que se expresen términos racistas, sexistas, incitadores de la violencia, entre otros. 
                                        <br>
                                        <br>
                                        Quedan tajantemente prohibidas las expresiones injuriosas, calumniosas o que denosten la imagen, honor y honra de personas físicas o jurídicas, en los términos previstos en la Ley Orgánica 1/1982, de 5 de mayo, de Protección Civil del Derecho al Honor, a la Intimidad Personal y Familiar y a la Propia Imagen. AUDITOSCOPIA se reserva el derecho de no aceptar las contribuciones redactadas en tales términos. 
                                    </li>
                                </ul>
                            </li>
                        </ul>
                    </li>
                    <li>
                        <strong>1.2 Aceptación de la contribución</strong>
                        <br>
                        <br>
                        AUDITOSCOPIA analizará la contribución propuesta por medio de sus moderadores en la fase de Revista, con una duración máxima de tres (3) días naturales.
                        <br>
                        <br>
                        Si cumple con los requisitos establecidos, la contribución será publicada en formato resumen. La propuesta completa será de carácter privado, accediendo a ella únicamente AUDITOSCOPIA y su propio autor. 
                        <br>
                        <br>
                        Al mismo tiempo, el resto de colaboradores podrán contribuir en el concurso con sus propias contribuciones y propuestas de mejora.
                    </li>
                    <li style="margin-top: 1.8em">
                        <strong>1.3 Fases y duración del  concurso</strong>
                        <br>
                        <br>
                        La duración del concurso se extenderá por un plazo de noventa (90) días que serán improrrogables.
                        <ul class="clean">
                            <li>
                                <strong>1.3.1 Concurso activo</strong>
                                <br>
                                <br>
                                El concurso estará activo desde inicio del mismo (una vez publicada la primera contribución) hasta la celebración del Referéndum. Este será el único periodo en el cual los colaboradores podrán realizar sus contribuciones.
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.2 Referéndum</strong>
                                <br>
                                <br>
                                El Referéndum comenzará una vez finalizado el concurso. Durante este periodo el concurso dejará de estar activo y por lo tanto no se podrán realizar nuevas contribuciones. El Referéndum se extenderá por el plazo de siete (7) días naturales y durante el mismo, los colaboradores podrán votar las contribuciones de los otros participantes, con el objeto de elegir las propuestas ganadoras. 
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.3 Derecho al voto</strong>
                                <br>
                                <br>
                                Podrán votar en los Referéndums únicamente los colaboradores que hayan realizado alguna contribución en el concurso en el que desean votar, y que ésta haya sido publicada. Estos usuarios se denominan electores. Los colaboradores de otros concursos o del mismo concurso cuyas contribuciones no hayan sido aceptadas no podrán votar. 
                                <br>
                                <br>
                                Cada elector tendrá derecho a emitir un máximo de cinco votos en cada Referéndum, no siendo obligatoria la ejecución de los cinco, aunque estará obligado a emitir al menos un voto con un valor de cinco (5), cuatro (4), tres (3), dos (2) ó un (1) punto de acuerdo a sus preferencias. 
                                <br>
                                <br>
                                Los votos se conforman del siguiente modo:
                                <ul>
                                    <li>
                                        1 voto de 5 puntos para la contribución que aporte más valor según su preferencia.
                                    </li>
                                    <li>
                                        1 voto de 4 puntos para la segunda contribución de más valor.
                                    </li>
                                    <li>
                                        1 voto de 3 puntos para la tercera contribución de más valor.
                                    </li>
                                    <li>
                                        1 voto de 2 puntos para la cuarta contribución de más valor.
                                    </li>
                                    <li>
                                        1 voto de 1 punto para la quinta contribución de más valor.
                                    </li>
                                </ul>
                                Los colaboradores no podrán votar sus propias contribuciones.
                                <br>
                                <br>
                                Cada elector podrá votar a una contribución (con 5, 4, 3, 2, ó 1) una sola vez por concurso.
                                <br>
                                <br>
                                El proceso de votación es personal y anónimo.
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.4 Quórum</strong>
                                <br>
                                <br>
                                Para que el Referéndum sea declarado válido será imprescindible la participación del 25% o más de los colaboradores que hayan contribuido en el concurso.
                                <br>
                                <br>
                                De esta forma, si en un concurso han contribuido ochenta (80) colaboradores el quórum mínimo sería  de veinte (20). 
                                <br>
                                <br>
                                El mínimo de electores requerido en cualquier caso será de diez (10) con un número de votos emitidos nunca inferior a veinticinco (25).
                                <br>
                                <br>
                                En caso de no alcanzarse este umbral, el concurso será declarado nulo.
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.5 Escrutinio y proclamación de ganadores</strong>
                                <br>
                                <br>
                                Habiéndose cumplido el quórum de participación y habiendo finalizado la votación, AUDITOSCOPIA procederá al recuento de los votos y hará públicas las tres (3) propuestas que más puntos hayan obtenido, las cuales serán las ganadoras del concurso.
                                <br>
                                <br>
                                Dichas propuestas, junto con las otras que hayan resultado del interés de los votantes, formarán parte del Plan de acción de mejora que será presentado a la empresa o entidad objeto del concurso. AUDITOSCOPIA determinará discrecionalmente el valor monetario con el que venda dicho proyecto.  
                                <br>
                                <br>
                                El escrutinio y publicación en la web de los resultados de las votaciones se realizará en el mismo momento de la finalización del Referéndum.
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.6 Deliberación</strong>
                                <br>
                                <br>
                                La fase de Deliberación tendrá una duración variable y en ella se estudiarán las siguientes acciones a tomar.
                                <br>
                                <br>
                                En caso de que el Plan de acción haya sido comprado por la empresa, entidad o fabricante del producto objeto del concurso, se procederá en este sentido.
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.7 Ganadores del concurso</strong>
                                <br>
                                <br>
                                Serán ganadores del concurso los tres primeros clasificados del mismo, es decir las tres contribuciones que más puntos hayan obtenido durante el Referéndum correspondiente. Además de puntos extra, los ganadores finales tendrán derecho a obtener Participación en beneficio en caso de venta de un Plan de acción.
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.8 Empates</strong>
                                <br>
                                <br>
                                Los empates se resolverán de la siguiente forma:
                                <ul class="clean">
                                    <li>
                                        <strong>1) Empate de más de una contribución en el 1er puesto</strong>
                                        <br>
                                        <br>
                                        El porcentaje de reparto de Participación en beneficio, asignado normalmente a la 2ª contribución más votada, se sumará a la asignada a la 1ª más votada. La cantidad resultante se dividirá, a partes iguales, entre las contribuciones que han empatado en el 1er puesto.
                                        <br>
                                        <br>
                                        La siguiente contribución más votada recibirá la recompensa asignada normalmente al 3er puesto.
                                        <br>
                                        <br>
                                        El resto del porcentaje de Participación en beneficio a repartir se dividirá entre las contribuciones con <?php echo link_to('derecho a Participación en beneficio', 'preguntasfrecuentes/recompensas#D', array('title' => '¿Quién tiene derecho a Participación en beneficio?')) ?>, a partes iguales.
                                    </li>
                                </ul>
                                <ul class="clean">
                                    <li>
                                        <strong>2) Empate de más de una contribución en el 2º puesto</strong>
                                        <br>
                                        <br>
                                        El porcentaje de reparto de Participación en beneficio, asignado normalmente a la 3ª contribución más votada, se sumará a la asignada a la 2ª más votada. La cantidad resultante se dividirá, a partes iguales, entre las contribuciones que han empatado en el 2º puesto.
                                        <br>
                                        <br>
                                        El resto del porcentaje de Participación en beneficio a repartir se dividirá entre las contribuciones con <?php echo link_to('derecho a Participación en beneficio', 'preguntasfrecuentes/recompensas#D', array('title' => '¿Quién tiene derecho a Participación en beneficio?')) ?>, a partes iguales.
                                    </li>
                                </ul>
                                <ul class="clean">
                                    <li>
                                        <strong>3) Empate de más de una contribución en el 3er puesto</strong>
                                        <br>
                                        <br>
                                        El porcentaje de reparto de Participación en beneficio, asignado normalmente a las contribuciones que han conseguido votos de al menos cinco (5) colaboradores y diez (10) puntos o más, se dividirá, a partes iguales, entre las clasificadas en el 3er puesto.
                                        <br>
                                        <br>
                                        El resto de colaboradores con derecho al cobro no percibirán <i>Participación en beneficio</i> en este caso.
                                    </li>
                                </ul>
                            </li>
                            <li>
                                <strong>1.3.9 Fase de Observación y cierre del concurso</strong>
                                <ul>
                                    <li>
                                        En el caso en que la empresa, entidad o fabricante del producto objeto del mismo compre el Plan de acción propuesto a través de la contratación de uno de los servicios prestados por AUDITOSCOPIA, el concurso pasará a fase de Observación hasta que la empresa o entidad objeto del mismo confirme la implantación con éxito de las medidas recomendadas. Una vez comprobado, se cerrará.
                                    </li>
                                    <li>
                                        Un concurso se cerrará en el momento en que, la empresa, entidad o fabricante del producto objeto del mismo, rechace el Plan de acción propuesto así como otro alternativo, en un plazo posterior de tres (3) meses, que resuelva las incidencias detectadas.
                                    </li>
                                    <li>
                                        Un concurso se cerrará en el momento en que sea declarado nulo por falta de quórum.
                                    </li>
                                </ul>
                            </li>
                            <li>
                                <strong>1.3.10 Nulidad de concurso</strong>
                                <br>
                                <br>
                                Un concurso será declarado nulo por las siguientes causas:
                                <ul>
                                    <li>
                                        Si el objeto del concurso es un pequeño negocio, entidad pequeña o producto de uso poco frecuente (menos de 2.500 clientes o usuarios estimados al año), en el caso de que no se hayan alcanzado al menos veinticinco (25) contribuciones de valor publicadas en el mismo.
                                    </li>
                                    <li>
                                        Si el objeto del concurso es una mediana empresa, entidad mediana o un producto de uso moderadamente frecuente (entre 2.500 y 10.000 clientes o usuarios estimados al año), en el caso de que no se hayan alcanzado al menos cien (100) contribuciones de valor.
                                    </li>
                                    <li>
                                        Si el objeto del concurso es una gran empresa, entidad grande o un producto de uso frecuente (más de 10.000 clientes o usuarios estimados al año), en el caso de que no se hayan alcanzado al menos doscientos cincuenta (250) contribuciones de valor.
                                    </li>
                                    <li>
                                        En cualquiera de los tres casos anteriores, cuando el Referéndum correspondiente no cuente con un quórum de participación de al menos el 25% de los colaboradores que hayan contribuido en el concurso con un una contribución o más publicada, con un mínimo de diez (10) votantes y veinticinco (25) votos emitidos.
                                    </li>
                                    <li>
                                        Por causas de fuerza mayor, caso fortuito o no imputables a AUDITOSCOPIA.
                                    </li>
                                </ul>
                                En el caso de que el concurso declarado nulo pertenezca a la modalidad de concursos de la Comunidad Privada, no se realizará el reparto de Participación en beneficio que se perderá sin posibilidad de compensación.
                            </li>
                            <li style="margin-top: 1.8em">
                                <strong>1.3.11 Publicación en las Listas blanca y negra</strong>
                                <br>
                                <br>
                                Serán publicados en la lista blanca las empresas, entidades o productos que hayan comprado el Plan de acción de mejora propuesto en un concurso y hayan implantado las medidas de mejora recomendadas.
                                <br>
                                <br>
                                Serán igualmente publicadas en la lista blanca las empresas, entidades o productos que hayan implantado un Plan de acción de mejora alternativo y equivalente, en un plazo de tres (3) meses posterior al cierre del concurso, que solucione las incidencias detectadas.
                                <br>
                                <br>
                                Serán publicados en la lista negra las empresas, entidades o productos que no hayan implantado el Plan de acción de mejora propuesto u otro alternativo equivalente, en un plazo de tres (3) meses posterior al cierre del concurso, que solucione las incidencias detectadas.
