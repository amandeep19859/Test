                    <li>
                        <strong><i>Concurso:</i></strong> proceso por el cual un grupo virtual de usuarios propone la mejora de un producto, servicio, empresa o entidad pública respecto del cual han tenido una experiencia de cliente insatisfactoria o <strong><i>incidencia</i></strong>. Cada concurso tiene una duración de noventa (90) días y finaliza con la celebración de una votación o  <strong><i>Referéndum</i></strong>, mediante el cual se votan las <strong><i>contribuciones</i></strong> de los <strong><i>colaboradores.</i></strong>
                    </li>
                    <li>
                        <strong><i>Colaborador:</i></strong> son <i>colaboradores</i> los usuarios debidamente registrados en AUDITOSCOPIA que realicen <strong><i>contribuciones.</i></strong>
                    </li>
                    <li>
                        <strong><i>Contribución:</i></strong> conforma una <i>contribución</i> el testimonio de una <strong><i>incidencia</i></strong> y su propuesta de mejora asociada <strong><i>(Plan de acción)</i></strong> o cualquier otra actividad en AUDITOSCOPIA recompensada con puntos.
                    </li>
                    <li>
                        <strong><i>Cuenta de colaborador:</i></strong> cuenta donde se acumulan las recompensas económicas y los puntos de un colaborador.
                    </li>
                    <li>
                        <strong><i>Electores:</i></strong> usuarios registrados como colaboradores y que hayan participado en el concurso que es objeto del <strong><i>Referéndum.</i></strong>
                    </li>
                    <li>
                        <strong><i>Experiencia de cliente:</i></strong> experiencia de consumo o percepción subjetiva de la calidad y el grado de satisfacción producido por el uso de un producto o servicio.
                    </li>
                    <li>
                        <strong><i>Hacer caja:</i></strong> hacer efectivo el dinero acumulado en la cuenta del colaborador.
                    </li>
                    <li>
                        <strong><i>Plan de acción:</i></strong> propuesta de mejora presentada por un colaborador en respuesta a una incidencia. A su vez, <i>Plan de acción</i> (o <i>Plan de acción final</i>)  es la propuesta de mejora que presenta AUDITOSCOPIA a la empresa o entidad objeto de concurso y que contiene las propuestas de mejora ganadoras de los colaboradores.
